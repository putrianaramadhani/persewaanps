    public String CekLogin(){
        if(username.isEmpty() || password.isEmpty()){
         return"Form belum lengkap";
        }else{
            try{
                Statement stat = koneksiPS.getKoneksi().createStatement();
                Data = stat.executeQuery("select*from login where user='"+username+"' and pass='"+password+"'");
               if (Data.next()){
                   akses=Data.getString("hakakses");
                   return akses;
               }else {
                   return "Gagal login";
               }
            }catch (SQLException salahe){
                return "Kesalahan:"+salahe;
               }
            }
        }
    
