    public void login(){
        username = jTextField1.getText();
        password = jPasswordField1.getText();
        hasil = CekLogin();
    if (hasil.equals("pegawai")){
        jPanel1.hide();
        jPanel2.show();
        pegawai();
        JOptionPane.showMessageDialog(null,"Selamat Datang Pegawai");
    }else if (hasil.equals("pemilik")){
        jPanel1.hide();
        jPanel2.show();
        pemilik();
        JOptionPane.showMessageDialog(null,"Selamat Datang Pemilik");
        
    }
    }
    
